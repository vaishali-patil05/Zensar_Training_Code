package PracticalAss2;

public class UseOfBreak {
	public static void main(String[] args) {
		
	
    int i=1;  
    while(i<=10){  
        if(i==5){  
            //using break statement  
            i++;  
            break;//it will break the loop  
        }  
        System.out.println(i);  
        i++;  
    }  
    }
	}
  

